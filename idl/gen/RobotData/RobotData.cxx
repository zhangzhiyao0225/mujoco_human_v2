size_t RobotData::getMaxCdrSerializedSize(
        size_t current_alignment)
{
    size_t initial_alignment = current_alignment;



    for(size_t a = 0; a < (24); ++a)
    {
        current_alignment += MotorState::getMaxCdrSerializedSize(current_alignment);}

    for(size_t a = 0; a < (2); ++a)
    {
        current_alignment += IMUData::getMaxCdrSerializedSize(current_alignment);}
    current_alignment += 8 + eprosima::fastcdr::Cdr::alignment(current_alignment, 8);


    current_alignment += 4 + eprosima::fastcdr::Cdr::alignment(current_alignment, 4);


    current_alignment += 1 + eprosima::fastcdr::Cdr::alignment(current_alignment, 1);


    current_alignment += AlarmState::getMaxCdrSerializedSize(current_alignment);

    return current_alignment - initial_alignment;
}

size_t RobotData::getCdrSerializedSize(
        const RobotData& data,
        size_t current_alignment)
{
    (void)data;
    size_t initial_alignment = current_alignment;



    for(size_t a = 0; a < data.motors().size(); ++a)
    {
            current_alignment += MotorState::getCdrSerializedSize(data.motors().at(a), current_alignment);
    }

    for(size_t a = 0; a < data.imu().size(); ++a)
    {
            current_alignment += IMUData::getCdrSerializedSize(data.imu().at(a), current_alignment);
    }
    current_alignment += 8 + eprosima::fastcdr::Cdr::alignment(current_alignment, 8);


    current_alignment += 4 + eprosima::fastcdr::Cdr::alignment(current_alignment, 4);


    current_alignment += 1 + eprosima::fastcdr::Cdr::alignment(current_alignment, 1);


    current_alignment += AlarmState::getCdrSerializedSize(data.alarms(), current_alignment);

    return current_alignment - initial_alignment;
}

